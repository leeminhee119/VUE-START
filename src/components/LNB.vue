<template>
  <nav :style="{ width: `${navWidth}px`}">
    <div class="header">
      <div class="user-profile"></div>
      Leon's Notion
    </div>
    <ul>
      <WorkspaceItem 
        v-for="workspace in workspaces"
        :key="workspace.id"
        :workspace="workspace" />
    </ul>
    <div class="actions">
      <div
        class="action"
        @click="$store.dispatch('workspace/createWorkspace')">
        <span class="material-icons">add</span> 새로운 페이지
      </div>
    </div>
    <div
      class="resize-handle"
      @dblclick="navWidth = 240"></div>
  </nav>
</template>

<script>
import interact from 'interactjs'
import WorkspaceItem from './WorkspaceItem'

export default {
  components: {
    WorkspaceItem,
  },
  data() {
    return {
      navWidth: 240
    }
  },
  computed: {
    workspaces() {
      return this.$store.state.workspace.workspaces
    }
  },
  created() {
    this.$store.dispatch('workspace/readWorkspaces')
  },
  mounted() {
    this.navInit()
  },
  methods: {
    navInit() {
      interact('nav')
        .resizable({
          edges: {
            right: '.resize-handle' // .resize-handle을 움직일 때만 resizemove 동작
          }
        })
        .on('resizemove', event => {
          this.navWidth = event.rect.width < 140 ? 140 : event.rect.width
        })
    }
  }
}
</script>

